/**
 * The exponentiation operator a ** b raises a to the power of b
 * Power of 1/2 is the same as square root
 * Power of 1/3 is the same as cubic root
 */

console.log(2 ** 3);    // 8
console.log(4 ** 1/2);  // 2
console.log(8 ** 1/3);  // 2

console.log(2 ^ 3);     // 1 - XOR bitwise
console.log(2 ^ 2);     // 0

console.log(3 % 2);     // 1 - reminder
console.log(8 % 3);     // 2